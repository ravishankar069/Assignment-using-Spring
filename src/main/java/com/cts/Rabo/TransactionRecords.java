package com.cts.Rabo;

public class TransactionRecords {
	
	private String ref, accNum, startBal, mut, desc, endBal;

	public String getRef() {
		return ref;
	}

	public void setRef(String ref) {
		this.ref = ref;
	}

	public String getAccNum() {
		return accNum;
	}

	public void setAccNum(String accNum) {
		this.accNum = accNum;
	}

	public String getStartBal() {
		return startBal;
	}

	public void setStartBal(String startBal) {
		this.startBal = startBal;
	}

	public String getMut() {
		return mut;
	}

	public void setMut(String mut) {
		this.mut = mut;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getEndBal() {
		return endBal;
	}

	public void setEndBal(String endBal) {
		this.endBal = endBal;
	}

}
